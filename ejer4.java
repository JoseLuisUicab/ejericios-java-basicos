package ejercicio1;

import java.util.Scanner;

/**
 ** ingresar 5numeros postivos en un arreglo y mostrarlos negativos
 * @author joseluis
 */
public class ejer4 {
    public static void main(String[] args) {
        Scanner p = new Scanner(System.in);
        int negativo = -1;
        int num[] = new int[5];
        
        for (int i = 0; i < num.length; i++) {
            System.out.print("ingresar los numeros: ");
            num[i]=p.nextInt();
            
        }
        for (int i = 0; i < num.length; i++) {
            num[i]*=negativo;
            System.out.println("numeros a negativos: "+num[i]);
            
        }
        
        
    }
    
}
